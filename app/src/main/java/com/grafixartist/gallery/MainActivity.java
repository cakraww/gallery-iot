    private void reloadImages() {
        data.clear();
        final String BASE_URL = getResources().getString(R.string.PI_URL);
        String url = BASE_URL + "/photos";
        JsonObjectRequest req = new JsonObjectRequest(Request.Method.GET, url, null, new Response.Listener<JSONObject>() {
            @Override
            public void onResponse(JSONObject response) {
                Log.d(TAG, response.toString());
                List<String> photoNames = new ArrayList<>();
                try {
                    JSONArray photosJA = response.getJSONArray("photos");
                    for (int i = 0; i < photosJA.length(); ++i) {
                        String photoName = photosJA.getString(i);
                        photoNames.add(photoName);
                    }

                    for (String photoName: photoNames) {
                        ImageModel imageModel = new ImageModel();
                        imageModel.setName(photoName);
                        imageModel.setUrl(BASE_URL + "/images/" + photoName);
                        data.add(imageModel);
                    }

                    mAdapter = new GalleryAdapter(MainActivity.this, data);
                    mRecyclerView.setAdapter(mAdapter);

                    mRecyclerView.addOnItemTouchListener(new RecyclerItemClickListener(getApplicationContext(),
                            new RecyclerItemClickListener.OnItemClickListener() {

                                @Override
                                public void onItemClick(View view, int position) {

                                    Intent intent = new Intent(MainActivity.this, DetailActivity.class);
                                    intent.putParcelableArrayListExtra("data", data);
                                    intent.putExtra("pos", position);
                                    startActivity(intent);

                                }
                            }));

                } catch (JSONException e) {
                    e.printStackTrace();
                    Toast.makeText(getApplicationContext(), "Error: " + e.getMessage(), Toast.LENGTH_LONG).show();
                }

            }

        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                Log.d(TAG, "Error: " + error.getMessage());
                Toast.makeText(getApplicationContext(), error.getMessage(), Toast.LENGTH_SHORT).show();
            }
        });

        AppController.getInstance().addToRequestQueue(req);
    }

